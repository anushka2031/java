public class neg_posi {

    // negative-positive-zero
    public static void main(String[] args) {
        
        int n=4;

        if(n>0){
            System.out.println("positive");
        }
        else{
            if(n==0){
                System.out.println("zero");
            }
            else{
                System.out.println("negative");
            }
        }
    }
    
}
